import { mkdirSync, writeFileSync } from "node:fs"
import path from "node:path"

const outputPath = path.resolve("src/lib/florida-geography.ts")

const sources = {
  placeGazetteer2025:
    "https://www2.census.gov/geo/docs/maps-data/data/gazetteer/2025_Gazetteer/2025_gaz_place_12.txt",
  countyGazetteer2025:
    "https://www2.census.gov/geo/docs/maps-data/data/gazetteer/2025_Gazetteer/2025_gaz_counties_12.txt",
  placeAnsi2020:
    "https://www2.census.gov/geo/docs/reference/codes2020/place/st12_fl_place2020.txt",
  placeCountyAnsi2020:
    "https://www2.census.gov/geo/docs/reference/codes2020/place_by_cou/st12_fl_place_by_county2020.txt",
}

function slug(value) {
  return value
    .toLowerCase()
    .trim()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
}

function parsePipe(text) {
  const [headerLine, ...rows] = text.trim().split(/\r?\n/)
  const headers = headerLine.split("|")

  return rows
    .filter(Boolean)
    .map((row) => {
      const cells = row.split("|")

      return Object.fromEntries(headers.map((header, index) => [header, cells[index] ?? ""]))
    })
}

function normalizePlaceName(value) {
  return value
    .replace(/\s+(city|town|village|CDP)$/i, "")
    .replace(/\s+/g, " ")
    .trim()
}

function placeKind(value) {
  if (/\scity$/i.test(value)) return "city"
  if (/\stown$/i.test(value)) return "town"
  if (/\svillage$/i.test(value)) return "village"

  return "cdp"
}

function countyName(value) {
  return value.replace(/\s+County$/i, "").trim()
}

async function fetchText(url) {
  const response = await fetch(url)
  if (!response.ok) throw new Error(`Failed to fetch ${url}: ${response.status}`)

  return response.text()
}

const [placeGazetteer, countyGazetteer, placeAnsi, placeCountyAnsi] = await Promise.all(
  Object.values(sources).map(fetchText),
)

const gazetteerPlacesByPlaceFp = new Map(
  parsePipe(placeGazetteer).map((record) => [record.GEOID.slice(2), record]),
)
const countiesByCountyFp = new Map(
  parsePipe(countyGazetteer).map((record) => [record.GEOID.slice(2), record]),
)
const placeCountyRowsByPlaceFp = new Map()

for (const record of parsePipe(placeCountyAnsi)) {
  const rows = placeCountyRowsByPlaceFp.get(record.PLACEFP) ?? []
  rows.push(record)
  placeCountyRowsByPlaceFp.set(record.PLACEFP, rows)
}

const counties = [...countiesByCountyFp.entries()]
  .map(([countyFp, record]) => {
    const name = countyName(record.NAME)

    return {
      name,
      slug: `${slug(name)}-county`,
      countyFp,
      geoid: record.GEOID,
      ansiCode: record.ANSICODE,
      latitude: Number(record.INTPTLAT),
      longitude: Number(record.INTPTLONG),
    }
  })
  .sort((a, b) => a.name.localeCompare(b.name))

const places = parsePipe(placeAnsi)
  .map((record) => {
    const gazetteer = gazetteerPlacesByPlaceFp.get(record.PLACEFP)
    const countiesForPlace = (placeCountyRowsByPlaceFp.get(record.PLACEFP) ?? [])
      .map((row) => {
        const county = countyName(row.COUNTYNAME)

        return {
          name: county,
          slug: `${slug(county)}-county`,
          countyFp: row.COUNTYFP,
        }
      })
      .sort((a, b) => a.name.localeCompare(b.name))
    const kind = placeKind(record.PLACENAME)
    const name = normalizePlaceName(record.PLACENAME)

    return {
      name,
      fullName: record.PLACENAME,
      slug: slug(name),
      kind,
      isMunicipality: record.TYPE === "INCORPORATED PLACE" && record.FUNCSTAT === "A",
      isCensusDesignatedPlace: record.TYPE === "CENSUS DESIGNATED PLACE",
      placeFp: record.PLACEFP,
      geoid: gazetteer?.GEOID ?? `12${record.PLACEFP}`,
      ansiCode: record.PLACENS,
      classFp: record.CLASSFP,
      funcstat: record.FUNCSTAT,
      counties: countiesForPlace,
      latitude: gazetteer ? Number(gazetteer.INTPTLAT) : null,
      longitude: gazetteer ? Number(gazetteer.INTPTLONG) : null,
    }
  })
  .sort((a, b) => a.name.localeCompare(b.name))

const body = `// Generated by scripts/generate-florida-geography.mjs.
// Sources:
// - U.S. Census 2025 Gazetteer Florida counties and places
// - U.S. Census 2020 ANSI/FIPS Florida place and place-by-county tables

export type FloridaPlaceKind = "city" | "town" | "village" | "cdp"

export interface FloridaCountyRecord {
  name: string
  slug: string
  countyFp: string
  geoid: string
  ansiCode: string
  latitude: number
  longitude: number
}

export interface FloridaPlaceCountyRef {
  name: string
  slug: string
  countyFp: string
}

export interface FloridaPlaceRecord {
  name: string
  fullName: string
  slug: string
  kind: FloridaPlaceKind
  isMunicipality: boolean
  isCensusDesignatedPlace: boolean
  placeFp: string
  geoid: string
  ansiCode: string
  classFp: string
  funcstat: string
  counties: FloridaPlaceCountyRef[]
  latitude: number | null
  longitude: number | null
}

export const floridaGeographySources = ${JSON.stringify(sources, null, 2)} as const

export const floridaCountyRecords = ${JSON.stringify(counties, null, 2)} as const satisfies readonly FloridaCountyRecord[]

export const floridaPlaceRecords = ${JSON.stringify(places, null, 2)} as const satisfies readonly FloridaPlaceRecord[]

const floridaCountyBySlug = new Map<string, FloridaCountyRecord>(floridaCountyRecords.map((county) => [county.slug, county]))
const floridaCountyByName = new Map<string, FloridaCountyRecord>(floridaCountyRecords.map((county) => [county.name.toLowerCase(), county]))
const floridaPlaceBySlug = new Map<string, FloridaPlaceRecord>(floridaPlaceRecords.map((place) => [place.slug, place]))
const floridaPlaceByName = new Map<string, FloridaPlaceRecord>(floridaPlaceRecords.map((place) => [place.name.toLowerCase(), place]))

export const floridaMunicipalities = floridaPlaceRecords.filter((place) => place.isMunicipality)
export const floridaCensusDesignatedPlaces = floridaPlaceRecords.filter((place) => place.isCensusDesignatedPlace)

export function getFloridaCounty(value?: string | null) {
  const normalized = (value ?? "").trim().toLowerCase()
  if (!normalized) return undefined

  return floridaCountyBySlug.get(normalized) ?? floridaCountyByName.get(normalized.replace(/-county$/, "").replace(/-/g, " "))
}

export function getFloridaPlace(value?: string | null) {
  const normalized = (value ?? "").trim().toLowerCase()
  if (!normalized) return undefined

  return floridaPlaceBySlug.get(normalized) ?? floridaPlaceByName.get(normalized.replace(/-/g, " "))
}

export function floridaPlacesByCountySlug(countySlug: string) {
  return floridaPlaceRecords.filter((place) => place.counties.some((county) => county.slug === countySlug))
}

export function floridaLocationLabel(place: FloridaPlaceRecord) {
  const countyLabel = place.counties.length === 1 ? \` / \${place.counties[0]?.name} County\` : ""
  const kindLabel = place.kind === "cdp" ? "CDP" : place.kind

  return \`\${place.name}, FL (\${kindLabel}\${countyLabel})\`
}
`

mkdirSync(path.dirname(outputPath), { recursive: true })
writeFileSync(outputPath, `${body}\n`)
console.log(`Wrote ${outputPath} with ${counties.length} counties and ${places.length} places.`)
